// Helper functions for filtering
export const filterByCategory = (category: string): Dish[] => {
  return testDishes.filter(dish => dish.category === category);
};

export const filterByPrice = (maxPrice: number): Dish[] => {
  return testDishes.filter(dish => dish.price <= maxPrice);
};

export const filterByDistance = (maxDistance: number): Dish[] => {
  return testDishes.filter(dish => dish.distance <= maxDistance);
};

export const filterByRating = (minRating: number): Dish[] => {
  return testDishes.filter(dish => dish.rating >= minRating);
};

export const sortByPopularity = (): Dish[] => {
  return [...testDishes].sort((a, b) => b.sales - a.sales);
};

export const sortByPrice = (ascending: boolean = true): Dish[] => {
  return [...testDishes].sort((a, b) => 
    ascending ? a.price - b.price : b.price - a.price
  );
};

export const searchDishes = (query: string): Dish[] => {
  const lowerQuery = query.toLowerCase();
  return testDishes.filter(dish => 
    dish.name.toLowerCase().includes(lowerQuery) ||
    dish.description.toLowerCase().includes(lowerQuery) ||
    dish.restaurantName.toLowerCase().includes(lowerQuery) ||
    dish.tags.some(tag => tag.toLowerCase().includes(lowerQuery))
  );
};